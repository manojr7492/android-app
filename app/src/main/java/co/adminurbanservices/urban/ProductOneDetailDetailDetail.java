package co.adminurbanservices.urban;

class ProductOneDetailDetailDetail {

    private int id;
    private String time;
    private String rate;



    ProductOneDetailDetailDetail(int id, String time, String rate) {
        this.id = id;
        this.time = time;
        this.rate = rate;

    }

    public int getId() {
        return id;
    }


    String getServicename() {
        return time;
    }

    public String getRate() {
        return rate;
    }


}
